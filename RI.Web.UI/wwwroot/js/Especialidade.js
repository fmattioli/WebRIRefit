function mostrarForm() {
    if ($('#formularioLivro').css('display') == 'none') {
        $('#formularioLivro').css('display', 'inline');
    }
    else {
        $('#formularioLivro').css('display', 'none');
    }
}

function editarLivro() {
    if ($('#formularioLivro').css('display') == 'none') {
        $('#formularioLivro').css('display', 'inline');
    }
    else {
        $('#formularioLivro').css('display', 'none');
    }
}
